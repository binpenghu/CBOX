#include"delay.h"

/*******************************************************************************
 *	Function:
 *	Parameters: None
 *	Returns: None
 *	Description:   us / 16mhz
 *******************************************************************************/
void delay_us(u16 count)
{ 
	while(--count)
	{
		nop();nop();nop();nop();
		nop();nop();nop();nop();
	}
}

/*******************************************************************************
 *	Function:
 *	Parameters: None
 *	Returns: None
 *	Description:   ms / 16mhz
 *******************************************************************************/
void delay_ms(u16 time)
{
    while(time--)  
    	delay_us(1000); 
}
