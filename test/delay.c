#include"delay.h"

/*******************************************************************************
 *	Function:
 *	Parameters: None
 *	Returns: None
 *	Description:   us / 16mhz
 *******************************************************************************/
void delay_us(u16 count)
{ 
    u16 temp = count;
	while(--temp)
	{
		nop();nop();nop();nop();nop();nop();nop();nop();
	}
}

/*******************************************************************************
 *	Function:
 *	Parameters: None
 *	Returns: None
 *	Description:   ms / 16mhz
 *******************************************************************************/
void delay_ms(u16 time)
{
    u16 temp = time;
    while(temp--)  
    {
    	delay_us(1000); 
    }
}
