u8 keyValTest=0;

/******************************************************************************
 *  Function:
 *  Parameters: None
 *  Returns: None
 *  Description:   
 ******************************************************************************/
 void key_test_task(void)
{
	static u8 temp = 0;
	temp = keyValTest;
	if (temp <=0)
		return;
	if (temp <=10)
	{
		dispaly_num(temp);
	}else if (temp <=12)
	{
		dispaly_num(100+temp);
	}else if (temp <= 13)
	{
		display_machineMode_led(ON);
		display_chargeFinish_led(OFF);
	}else if (temp <= 14)
	{
		display_machineMode_led(OFF);
		display_chargeFinish_led(ON);
	}else if (temp <= 22)
	{
		display_led((temp-15),ON);
	}else
	{
		keyValTest = 0;
	}
}

